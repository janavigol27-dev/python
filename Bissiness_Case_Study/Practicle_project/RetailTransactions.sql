SELECT SalesChannel, DATE_FORMAT(Date, '%Y-%m') AS Month, SUM(TotalAmount) AS Sales 
FROM RetailTransactions 
GROUP BY SalesChannel, Month 
ORDER BY Month;

-- 6. Category-wise sales trend (to identify rising/falling categories)
SELECT Category, SUM(TotalAmount) AS Category_Revenue 
FROM RetailTransactions 
GROUP BY Category 
ORDER BY Category_Revenue DESC;

-- 7. High-value customers with more than 10 transactions
SELECT CustomerID, COUNT(TransactionID) AS Total_Orders 
FROM RetailTransactions 
GROUP BY CustomerID 
HAVING Total_Orders > 10;

