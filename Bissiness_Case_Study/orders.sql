-- Monthly Revenue Report
SELECT 
    DATE_FORMAT(o.order_date, '%Y-%m') AS Month, 
    SUM(oi.quantity * p.unit_price) AS Monthly_Revenue
FROM orders o
JOIN order_items oi ON o.order_id = oi.order_id
JOIN products p ON oi.product_id = p.product_id
GROUP BY Month
ORDER BY Month;

